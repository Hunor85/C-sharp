            Console.WriteLine("---------- UInt ----------");
            uint uintMinValue = uint.MinValue;
            uint uintMaxValue = uint.MaxValue;
            Console.WriteLine("The Minimum value for UInt is {0}: ", uintMinValue);
            Console.WriteLine("The Maximum value for UInt is {0}: ", uintMaxValue);

