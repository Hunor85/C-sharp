            ushort ushortMinValue = ushort.MinValue;
            ushort ushortMaxValue = ushort.MaxValue;
            Console.WriteLine("The Minimum value for UShort is {0}: ", ushortMinValue);
            Console.WriteLine("The Maximum value for UShort is {0}: ", ushortMaxValue);

